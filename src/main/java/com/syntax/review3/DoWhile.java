package com.syntax.review3;

public class DoWhile {

    public static void main(String[] args) {

        int a=4;

        do{
            System.out.println("Bye");
            a--;
        } while(a>6);

        System.out.println("   ----------------  ");

        int b=4;

        while(b>6){
            System.out.println("Bye");
        }
    }
}
